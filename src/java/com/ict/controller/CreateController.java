    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
            String username = request.getParameter("username");
            String address = request.getParameter("address");
            String position = request.getParameter("position");
            String department = request.getParameter("department");
            
            NewJerseyClient client = new NewJerseyClient();
            GenericType<List<Employee>> gType = new GenericType<List<Employee>>(){      
            };
                Employee employee = new Employee(username, address, position, department);
                client.create_XML(employee);
                List<Employee> emp = (List<Employee>) client.findAll_XML(gType);
                request.setAttribute("ListEmp", emp);
                request.getRequestDispatcher("index.jsp").forward(request, response);
//processRequest(request, response);
    }

