    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String idstr = request.getParameter("id");
        Integer id = Integer.parseInt(idstr);
        NewJerseyClient client = new NewJerseyClient();
        GenericType<List<Employee>> gType = new GenericType<List<Employee>>(){      
        };
        
        List<Employee> emp = (List<Employee>) client.findAll_XML(gType);
        for (int i = 0; i < emp.size(); i++) {
            if(emp.get(i).getEmpid().equals(id)) {
                request.setAttribute("username", emp.get(i).getEmpid());
                request.setAttribute("username", emp.get(i).getFullname());
                request.setAttribute("address", emp.get(i).getAddress());
                request.setAttribute("position", emp.get(i).getPosition());
                request.setAttribute("department", emp.get(i).getDepartment());
            }
        }
        request.getRequestDispatcher("form.jsp").forward(request, response);
        //processRequest(request, response);
    }

